    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        try {
            st = cn.createStatement();
            if(Integer.parseInt(jumlah.getText()) >= Integer.parseInt(tableMenu.getValueAt(tableMenu.getSelectedRow(), 3).toString())) {
                JOptionPane.showMessageDialog(this, "Jumlah pesanan melebihi stok!!");
            } else {
                double hasil =  Double.parseDouble(harga.getText())  * Double.parseDouble(jumlah.getText());
                int total = (int) Math.round(hasil);
                
                int stokNew = Integer.parseInt(tableMenu.getValueAt(tableMenu.getSelectedRow(), 3).toString()) - Integer.parseInt(jumlah.getText());

                System.out.println(idTransaksi);

                st.executeUpdate("INSERT INTO pesanan(menu_id, transaksi_id, jumlah, total, created_at, updated_at) VALUES('"+ menu_id.getText() +"', '"+ idTransaksi +"', '"+ jumlah.getText() +"', '"+ total +"', '"+ now +"', '"+ now +"')");
                st.executeUpdate("UPDATE menu SET stok = '"+ stokNew +"' WHERE id = '"+ menu_id.getText() +"'");
                JOptionPane.showMessageDialog(this, "Menu berhasil ditambahkan");

                jumlah.setText("");
                tampilDataKeranjang();
                tampilData("");
            }

        } catch(Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnTambahActionPerformed

    private void btnDaftarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDaftarActionPerformed
        try {
            st = cn.createStatement();
            st.executeUpdate("INSERT INTO transaksi(nama, meja, total, created_at, updated_at) VALUES('"+ nama.getText() +"', '"+ meja.getSelectedItem() +"', 0, '"+ now +"', '"+ now +"')");
            st.executeUpdate("UPDATE meja SET status = 0  WHERE id = '"+ meja.getSelectedItem() +"'");
            rs = st.executeQuery("SELECT id FROM transaksi WHERE nama = '"+ nama.getText() +"'");
            while(rs.next()) {
                idTransaksi = rs.getString("id");
            };

            JOptionPane.showMessageDialog(this, "Pelanggan berhasil di daftarkan");
            nama.setEnabled(false);
            meja.setEnabled(false);
            btnDaftar.setEnabled(false);
            tampilData("");
            enabled();

        } catch(Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnDaftarActionPerformed

    private void btnKonfirmasiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKonfirmasiActionPerformed
        try {
            st = cn.createStatement();
            rs = st.executeQuery("SELECT * FROM pesanan WHERE transaksi_id = '"+ idTransaksi +"'");

            int total = 0;
            while(rs.next()) {
                total += rs.getInt("total");
            };

            st.executeUpdate("UPDATE transaksi SET total = '"+ total +"' where id = '"+ idTransaksi +"'");

            JOptionPane.showMessageDialog(this, "Menu berhasil di order");
            resetAll();
            tampilData("");
            tampilDataMeja();
            tampilDataKeranjang();
            tampilDataPesanan("");

        } catch(Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnKonfirmasiActionPerformed

