    public void tampilDataPesanan(String where) {
        try {
            st = cn.createStatement();
            tabModelPesanan.getDataVector().removeAllElements();
            tabModelPesanan.fireTableDataChanged();
            rs = st.executeQuery("SELECT nama, meja, transaksi_id FROM transaksi INNER JOIN pesanan ON transaksi.id = pesanan.transaksi_id WHERE pesanan.status = 'DIBUAT' GROUP BY nama" + where);
            
            while (rs.next()) {
                Object[] data = {
                    rs.getString("transaksi_id"),
                    rs.getString("nama"),
                    rs.getString("meja"),
                };
                
                tabModelPesanan.addRow(data);
            }
        } catch(Exception e) {
            e.printStackTrace();
        }
    }
    
    public void tampilDetailPesanan(String id) {
        try {
            st = cn.createStatement();
            rs = st.executeQuery("SELECT pesanan.id, menu.nama_menu, menu.harga, pesanan.jumlah FROM pesanan INNER JOIN menu ON pesanan.menu_id = menu.id where pesanan.transaksi_id = '"+ id +"'");
            
            listDetailPesanan.removeAll();
            
            while(rs.next()) {
                listDetailPesanan.addItem(rs.getString("nama_menu") + "              " + "x" +rs.getString("jumlah"));
            };
            
            
        } catch(Exception e) {
            e.printStackTrace();
        }
    }
    
    public void tampilDataMeja() {
        try {
            st = cn.createStatement();
            rs = st.executeQuery("SELECT * FROM meja");
            meja.removeAllItems();
            while (rs.next()) {
                int status = Integer.parseInt(rs.getString("status"));
                if(status == 1) {
                    meja.addItem(rs.getString("id"));
                } 
            }
        } catch(Exception e) {
            e.printStackTrace();
        }
    }
    
