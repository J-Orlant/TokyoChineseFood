    public void tampilDataKeranjang() {
        try {
            st = cn.createStatement();
            tabModelKeranjang.getDataVector().removeAllElements();
            tabModelKeranjang.fireTableDataChanged();
            rs = st.executeQuery("SELECT pesanan.id, menu.nama_menu, menu.harga, pesanan.jumlah FROM pesanan INNER JOIN menu ON pesanan.menu_id = menu.id where pesanan.transaksi_id = '"+ idTransaksi +"'");

            while(rs.next()) {
                Object[] data = {
                    rs.getString("id"),
                    rs.getString("nama_menu"),
                    rs.getString("harga"),
                    rs.getString("jumlah"),
                };
                tabModelKeranjang.addRow(data);
            };

        } catch(Exception e) {
            e.printStackTrace();
        }
    }
    
