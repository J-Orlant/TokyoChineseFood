    private void btnProsesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProsesActionPerformed
        int total = Integer.parseInt(txtTotal.getText());
        int bayar = Integer.parseInt(inputBayar.getText());

        if(bayar >= total) {
            int hasil = total - bayar;

            StringBuilder sb = new StringBuilder(Integer.toString(hasil));
            if(sb.length() > 1) {
                sb.deleteCharAt(0);
            }

            txtKembalian.setText(sb.toString());

            btnKonfirmasi.setEnabled(true);
        } else {
            JOptionPane.showMessageDialog(this, "Duit pembayaran kurang!!");
        }
    }//GEN-LAST:event_btnProsesActionPerformed

